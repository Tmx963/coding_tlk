# create a list
colors = ['red', 'blue', 'green', 'yellow', 'purple']
# Slice the list to get the second and third colors
Sliced_colores = colors[1:3] # blue & green
# print the sliced list
print(Sliced_colores)
# print the length of the original list
print(len(colors))