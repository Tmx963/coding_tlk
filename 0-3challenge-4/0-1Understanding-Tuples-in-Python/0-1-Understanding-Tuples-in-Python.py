# Creating a tuple
fruit_tuple = ("apple", "banana", "cherry")
print(fruit_tuple)  # Output: ('apple', 'banana', 'cherry')

# Accessing elements
print(fruit_tuple[1])  # Output: 'banana'

# Tuple unpacking
a, b, c = fruit_tuple
