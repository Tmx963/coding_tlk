#  Ask the user to enter a number.
# Store the number in a variable
user_input = int(input("Enter A Number: "))

# #Use conditional statements to check if the number is even or odd.
if user_input % 2 == 0:
    print("Even")
if user_input % 2 != 0:
    print("odd")



